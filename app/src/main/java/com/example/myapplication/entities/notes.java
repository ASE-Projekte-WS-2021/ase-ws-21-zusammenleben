package com.example.myapplication.entities;

public class notes {

    String title;
    String subtitle;
    String notice;
    String url;
    String imagePath;

    public String getUrl() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String getNotice() {
        return notice;
    }

    public String getImagePath() {
        return imagePath;
    }
}
